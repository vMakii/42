int	ft_isalnum(char c)
{
	return ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || (c >= 'A'
			&& c <= 'Z'));
}

#include <stdio.h>

int	main(void)
{
	char	c;

	c = 'a';
	printf("%d", ft_isalnum(c));
}
