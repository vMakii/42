int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

#include <stdio.h>

int	main(void)
{
	char	c;

	c = 'c';
	printf("%d", ft_isalpha(c));
}
